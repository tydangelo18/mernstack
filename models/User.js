// Bring in Mongoose
const mongoose = require('mongoose');

// Create a User Schema
const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  avatar: {
    type: String,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

// Export Schema to be used
module.exports = User = mongoose.model('user', UserSchema);
